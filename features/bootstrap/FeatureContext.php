class FeatureContext extends MinkContext
{
    /**
     * Initializes context.
     * Every scenario gets its own context object.
     *
     * @param array $parameters context parameters (set them up through behat.yml)
     */
    public function __construct(array $parameters)
    {
        // Initialize your context here
    }

    /**
     * @Given /^I should find:$/
     */
    public function iFindWherePosobotaIs(PyStringNode $location)
    {
        $page = $this->getSession()->getPage();
        $where = $page->find('css', '.event-location-name');

        if($where->getText() != (string)$location){
            throw new ErrorException('Wrong location, should be: '.(string)$location. ' and is: '.$where->getText());
        }
    }
}
